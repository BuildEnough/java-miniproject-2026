-- =====================================================
-- 3. 테이블 생성
-- =====================================================

-- 카테고리 테이블
CREATE TABLE CATEGORY (
    CATEGORY_ID NUMBER PRIMARY KEY,
    CATEGORY_NAME VARCHAR2(50) NOT NULL
);

-- 회원 테이블
CREATE TABLE USERS (
    USER_ID NUMBER PRIMARY KEY,
    LOGIN_ID VARCHAR2(50) NOT NULL UNIQUE,
    PASSWORD VARCHAR2(100) NOT NULL,
    NAME VARCHAR2(100) NOT NULL,
    ROLE VARCHAR2(30) NOT NULL,
    CREATED_AT DATE DEFAULT CURRENT_DATE NOT NULL,
    UPDATED_AT DATE
);

-- 자유게시판 테이블
CREATE TABLE C_BOARD (
    POST_ID NUMBER PRIMARY KEY,
    TITLE VARCHAR2(200) NOT NULL,
    CONTENTS CLOB NOT NULL,
    WRITER VARCHAR2(100),
    ANONYMOUS NUMBER(1) DEFAULT 0 NOT NULL,
    VIEW_COUNT NUMBER DEFAULT 0,
    CREATED_AT DATE DEFAULT CURRENT_DATE NOT NULL,
    UPDATED_AT DATE,
    USER_ID NUMBER NOT NULL,
    CONSTRAINT FK_CBOARD_USER
        FOREIGN KEY (USER_ID)
        REFERENCES USERS(USER_ID)
);

-- 거래게시판 테이블
CREATE TABLE B_BOARD (
    POST_ID NUMBER PRIMARY KEY,
    CATEGORY_ID NUMBER NOT NULL,
    TITLE VARCHAR2(200) NOT NULL,
    CONTENTS CLOB NOT NULL,
    VIEW_COUNT NUMBER DEFAULT 0,
    IMAGE_PATH VARCHAR2(500),
    CREATED_AT DATE DEFAULT CURRENT_DATE NOT NULL,
    UPDATED_AT DATE,
    USER_ID NUMBER NOT NULL,
    CONSTRAINT FK_BBOARD_CATEGORY
        FOREIGN KEY (CATEGORY_ID)
        REFERENCES CATEGORY(CATEGORY_ID),
    CONSTRAINT FK_BBOARD_USER
        FOREIGN KEY (USER_ID)
        REFERENCES USERS(USER_ID)
);

-- 거래게시판 댓글 테이블
CREATE TABLE COMMENTS (
    COMMENT_ID NUMBER PRIMARY KEY,
    CONTENTS VARCHAR2(1000) NOT NULL,
    CREATED_AT DATE DEFAULT CURRENT_DATE NOT NULL,
    UPDATED_AT DATE,
    POST_ID NUMBER NOT NULL,
    CATEGORY_ID NUMBER NOT NULL,
    USER_ID NUMBER NOT NULL,
    CONSTRAINT FK_COMMENT_CATEGORY
        FOREIGN KEY (CATEGORY_ID)
        REFERENCES CATEGORY(CATEGORY_ID),
    CONSTRAINT FK_COMMENT_USER
        FOREIGN KEY (USER_ID)
        REFERENCES USERS(USER_ID),
    CONSTRAINT FK_COMMENT_BBOARD
        FOREIGN KEY (POST_ID)
        REFERENCES B_BOARD(POST_ID)
        ON DELETE CASCADE
);

-- 자유게시판 댓글/답글 테이블
CREATE TABLE REPLY (
    REPLY_ID NUMBER PRIMARY KEY,
    CONTENTS VARCHAR2(1000) NOT NULL,
    WRITER VARCHAR2(100),
    CREATED_AT DATE DEFAULT CURRENT_DATE NOT NULL,
    UPDATED_AT DATE,
    POST_ID NUMBER NOT NULL,
    USER_ID NUMBER NOT NULL,
    CONSTRAINT FK_REPLY_CBOARD
        FOREIGN KEY (POST_ID)
        REFERENCES C_BOARD(POST_ID)
        ON DELETE CASCADE,
    CONSTRAINT FK_REPLY_USER
        FOREIGN KEY (USER_ID)
        REFERENCES USERS(USER_ID)
        ON DELETE CASCADE
);


-- =====================================================
