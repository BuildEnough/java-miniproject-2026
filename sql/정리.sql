-- 거래게시판 PK용
CREATE SEQUENCE B_BOARD_SEQ
START WITH 1
INCREMENT BY 1
NOCACHE
NOCYCLE;

-- 거래게시판 댓글 PK용
CREATE SEQUENCE COMMENTS_SEQ
START WITH 1
INCREMENT BY 1
NOCACHE
NOCYCLE;

-- 자유게시판 댓글/답글 PK용
CREATE SEQUENCE SEQ_REPLY_ID
START WITH 1
INCREMENT BY 1
NOCACHE
NOCYCLE;


